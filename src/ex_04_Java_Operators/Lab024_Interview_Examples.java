package ex_04_Java_Operators;

public class Lab024_Interview_Examples {
//    Covered in this class Lab045 to Lab047 - Dec 22, 2025
    static void main() {
        System.out.println(10==10);
        System.out.println(10==11);
//    Results reversed due to NOT operator
        System.out.println(!(10>20));
        System.out.println(!(10<20));
        System.out.println("Examples of OR operator");
        System.out.println(true||false);
        System.out.println(false||false);
        System.out.println("Examples of AND Operator");
        System.out.println(true&&false);
        System.out.println(false&&false);
        System.out.println(true&&true);
    }
}
