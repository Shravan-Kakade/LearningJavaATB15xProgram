package ex_07_Increment_Decrement_Operator;

public class Lab051_Decrement_2 {
    //    Covered in this class Lab078 - 26 Dec 2025
    static void main() {
        int a = 10;
        System.out.println(a);
        int result = --a;   // Here decrement in the value of a will happen first and then it will be assigned
        System.out.println(a);
        System.out.println(result);
    }

}
