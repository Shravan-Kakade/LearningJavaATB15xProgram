package ex_02_Java_Basics;

public class Lab004_Comments {
    //  > this is single line comment
    //  > commented code will not be executed while executing program

    /*
        *   To add multiline comment this syntax is used
        *   Multiline comment is used for documentation about code
        *   While programming combination of both single line and multiline comment can be used
        *   This class covered lab exercises from 04 to 08
    */

    public static void main() {

        System.out.println("Added statement before comment");
        // After adding comment or before it, program can be written
        System.out.println("Adding statement after comment");

        System.out.println("Shortcuts for adding comments are as follows");
        System.out.println("using ctrl + / to add single line comment");
        System.out.println("using ctrl + shift + / to add multi line comment");
    }
}
