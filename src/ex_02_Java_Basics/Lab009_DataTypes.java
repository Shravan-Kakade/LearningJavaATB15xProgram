package ex_02_Java_Basics;

public class Lab009_DataTypes {
    //Covered in this Class from Lab021 to Lab023
    static void main() {
        byte b = 10;
        short s = 10000;
        int i = 1234567891;
        long l = 1234567891098765432L; // while assigning value to variable of type long literal L is added after value
        char c = 'a';
        float f = 3.14f; // while assigning value to variable of type float literal f is added after value
        double d = 99.133256;
        System.out.println("Value of b is " + b);
        System.out.println("value of s is " + s);
        System.out.println("value of i is " + i);
        System.out.println("Value of l is " + l);
        System.out.println("value of c is " + c);
        System.out.println("value of f is " + f);
        System.out.println("value of d is " + d);
    }
}
