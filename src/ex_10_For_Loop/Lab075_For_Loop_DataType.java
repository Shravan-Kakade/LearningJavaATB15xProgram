package ex_10_For_Loop;

public class Lab075_For_Loop_DataType {
//    Covered in this class Lab107 - 05 Jan 2026
//    What type of data can be used in for loop? >>
//      Any type of primitive datatype can be used in 'for loop'.
static void main() {
    for (long i = 1l; i < 10; i++) {
        System.out.println(i);
    }
    for (float f = 0.0f; f < 10.67; f++) {
        System.out.println("Hi,Float -> " + f);
    }
}
}
